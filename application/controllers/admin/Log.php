	 /**
     * Save log
	 *@param $id - primary key to update
	 *
     */
    function save($id=-1){   
		 
		$created_at = "";
$updated_at = "";

		if($id<=0){
															 $created_at = date("Y-m-d H:i:s");
														 }
else if($id>0){
															 $updated_at = date("Y-m-d H:i:s");
														 }

		$params = array(
					 'users_id' => html_escape($this->input->post('users_id')),
'action' => html_escape($this->input->post('action')),
'ip' => html_escape($this->input->post('ip')),
'created_at' =>$created_at,
'updated_at' =>$updated_at,

				);
		 
		if($id>0){
							                        unset($params['created_at']);
						                          }if($id<=0){
							                        unset($params['updated_at']);
						                          } 
		$data['id'] = $id;
		//update		
        if(isset($id) && $id>0){
			$data['log'] = $this->Log_model->get_log($id);
            if(isset($_POST) && count($_POST) > 0){   
                $this->Log_model->update_log($id,$params);
                redirect('admin/log/index');
            }else{
                $data['_view'] = 'admin/log/form';
                $this->load->view('layouts/admin/body',$data);
            }
        } //save
		else{
			if(isset($_POST) && count($_POST) > 0){   
                $log_id = $this->Log_model->add_log($params);
                redirect('admin/log/index');
            }else{  
			    $data['log'] = $this->Log_model->get_log(0);
                $data['_view'] = 'admin/log/form';
                $this->load->view('layouts/admin/body',$data);
            }
		}
        
    } 
	
